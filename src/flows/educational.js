function buildCardText(card, view) {
  let t = `${card.emoji} *${card.title}*\n━━━━━━━━━━━━━━━━━━━━━━━━━━━\n\n`;

  switch (view) {
    case "keypoints":
      t += "🎯 *نکات کلیدی:*\n\n";
      card.keyPoints.forEach(function (p, i) {
        t += `${i + 1}. ${p}\n\n`;
      });
      break;

    case "mistakes":
      t += "❌ *اشتباهات رایج:*\n\n";
      card.commonMistakes.forEach(function (m) {
        t += `• ${m}\n\n`;
      });
      break;

    case "exercises":
      t += "📝 *تمرین‌های عملی:*\n\n";
      card.practicalExercises.forEach(function (e, i) {
        t += `${i + 1}. ${e}\n\n`;
      });
      break;

    case "tips":
      t += "💡 *نکات طلایی حرفه‌ای:*\n\n";
      card.proTips.forEach(function (tip) {
        t += `💎 ${tip}\n\n`;
      });
      break;

    default:
      // summary — متن اصلی content
      // حذف ۲ خط اول (عنوان تکراری)
      var lines = card.content.split("\n");
      t += lines.slice(2).join("\n");
      break;
  }

  return t;
}

