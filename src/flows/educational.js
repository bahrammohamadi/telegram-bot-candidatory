/** نمایش لیست آموزش‌ها */
export async function handleShowEducationList(ctx) {
  let t = "";
  t += "📚 *آموزش‌های تخصصی کاندیداتوری*\n";
  t += "━━━━━━━━━━━━━━━━━━━━━━━━━━━\n\n";
  t += "هر کارت شامل نکات کلیدی و کاربردی برای موفقیت در انتخابات است.\n\n";
  t += "موضوع مورد نظر را انتخاب کنید:\n\n";
  for (const c of CARDS) {
    t += `${c.emoji} ${c.title}\n`;
  }
  t += "\n💡 _مطالعه تمام کارت‌ها توصیه می‌شود._";

  const kb = listKB();

  if (ctx.callbackQuery) {
    try {
      await ctx.editMessageText(t, { parse_mode: "Markdown", reply_markup: kb });
    } catch {
      await ctx.reply(t, { parse_mode: "Markdown", reply_markup: kb });
    }
    await ctx.answerCallbackQuery();
  } else {
    await ctx.reply(t, { parse_mode: "Markdown", reply_markup: kb });
  }
}

/** نمایش یک کارت آموزشی */
export async function handleShowEducationCard(ctx, index) {
  if (index < 0 || index >= CARDS.length) {
    if (ctx.callbackQuery) await ctx.answerCallbackQuery({ text: "❌ کارت یافت نشد" });
    return;
  }

  const card = CARDS[index];
  const kb = navKB(index);

  if (ctx.callbackQuery) {
    try {
      await ctx.editMessageText(card.content, { parse_mode: "Markdown", reply_markup: kb });
    } catch {
      await ctx.reply(card.content, { parse_mode: "Markdown", reply_markup: kb });
    }
    await ctx.answerCallbackQuery();
  } else {
    await ctx.reply(card.content, { parse_mode: "Markdown", reply_markup: kb });
  }
}
