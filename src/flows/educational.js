/** کیبورد ناوبری (قبلی/شماره/بعدی) */
function navKB(idx) {
  const kb = new InlineKeyboard();

  const hasPrev = idx > 0;
  const hasNext = idx < CARDS.length - 1;

  if (hasPrev) kb.text("⬅️ قبلی", `edu:${idx - 1}`);
  kb.text(`📄 ${idx + 1}/${CARDS.length}`, "edu:noop");
  if (hasNext) kb.text("بعدی ➡️", `edu:${idx + 1}`);
  kb.row();

  kb.text("📚 فهرست موضوعات", "edu:list").row();
  kb.text("💼 بسته‌های خدماتی", "show_plans").row();
  kb.text("🔙 منوی اصلی", "menu").row();

  return kb;
}

/** کیبورد لیست موضوعات */
function listKB() {
  const kb = new InlineKeyboard();
  for (const c of CARDS) {
    kb.text(`${c.emoji} ${c.title}`, `edu:${c.id}`).row();
  }
  kb.text("🔙 منوی اصلی", "menu").row();
  return kb;
}

