function cardKB(cardId, currentView) {
  var kb = new InlineKeyboard();

  // ─── ردیف ۱ تب‌ها: خلاصه + نکات + اشتباهات ───
  VIEWS.slice(0, 3).forEach(function (v) {
    var label = currentView === v ? `[${VIEW_LABELS[v]}]` : VIEW_LABELS[v];
    kb.text(label, `eduv:${cardId}:${v}`);
  });
  kb.row();

  // ─── ردیف ۲ تب‌ها: تمرین + طلایی ───
  VIEWS.slice(3).forEach(function (v) {
    var label = currentView === v ? `[${VIEW_LABELS[v]}]` : VIEW_LABELS[v];
    kb.text(label, `eduv:${cardId}:${v}`);
  });
  kb.row();

  // ─── ناوبری قبلی / شمارنده / بعدی ───
  var idx = CARDS.findIndex(function (c) {
    return c.id === cardId;
  });
  if (idx > 0) kb.text("⬅️ قبلی", `edu:${CARDS[idx - 1].id}`);
  kb.text(`📖 ${idx + 1}/${TOTAL_CARDS}`, "edu_noop");
  if (idx < TOTAL_CARDS - 1) kb.text("بعدی ➡️", `edu:${CARDS[idx + 1].id}`);
  kb.row();

  // ─── دکمه‌های ثابت ───
  kb.text("📚 فهرست موضوعات", "edu_list").row();
  kb.text("💼 بسته‌ها", "show_plans").text("🔙 منو", "menu").row();

  return kb;
}

function listKB() {
  var kb = new InlineKeyboard();
  CARDS.forEach(function (c) {
    kb.text(`${c.emoji} ${c.title}`, `edu:${c.id}`).row();
  });
  kb.text("🔙 منوی اصلی", "menu").row();
  return kb;
}

