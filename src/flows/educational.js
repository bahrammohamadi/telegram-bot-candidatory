/**
 * کیبورد ناوبری کارت آموزشی
 */
function cardNavKB(idx) {
  const kb = new InlineKeyboard();

  const prev = idx > 0;
  const next = idx < CARDS.length - 1;

  if (prev) kb.text("⬅️ قبلی", `edu:${idx - 1}`);
  kb.text(`📖 ${idx + 1}/${CARDS.length}`, "edu_noop");
  if (next) kb.text("بعدی ➡️", `edu:${idx + 1}`);
  kb.row();

  kb.text("📚 فهرست موضوعات", "edu_list").row();
  kb.text("💼 بسته‌های خدماتی", "show_plans").row();
  kb.text("🔙 منوی اصلی", "menu").row();

  return kb;
}

/**
 * کیبورد فهرست موضوعات
 */
function listKB() {
  const kb = new InlineKeyboard();
  for (const c of CARDS) {
    kb.text(`${c.emoji} ${c.title}`, `edu:${c.id}`).row();
  }
  kb.text("🔙 منوی اصلی", "menu").row();
  return kb;
}

/**
 * نمایش فهرست آموزش‌ها
 */
export async function handleShowEducationList(ctx) {
  let t = "";
  t += "📚 *آموزش‌های تخصصی کاندیداتوری*\n";
  t += "━━━━━━━━━━━━━━━━━━━━━━━━━━━\n\n";
  t += "🎓 مجموعه‌ای از *نکات کلیدی و کاربردی* که هر کاندیدا باید بداند.\n\n";
  t += "موضوع مورد نظر را انتخاب کنید:\n\n";

  for (const c of CARDS) {
    t += `${c.emoji} *${c.title}*\n`;
  }

  t += "\n💡 _هر کارت شامل نکات عملی، مثال واقعی و تمرین‌های کاربردی است._";

  const kb = listKB();

  if (ctx.callbackQuery) {
    try {
      await ctx.editMessageText(t, { parse_mode: "Markdown", reply_markup: kb });
    } catch {
      await ctx.reply(t, { parse_mode: "Markdown", reply_markup: kb });
    }
    await ctx.answerCallbackQuery();
  } else {
    await ctx.reply(t, { parse_mode: "Markdown", reply_markup: kb });
  }
}

/**
 * نمایش یک کارت آموزشی
 */
export async function handleShowEducationCard(ctx, index) {
  if (index < 0 || index >= CARDS.length) {
    if (ctx.callbackQuery) await ctx.answerCallbackQuery({ text: "❌ کارت یافت نشد" });
    return;
  }

  const card = CARDS[index];
  const kb = cardNavKB(index);

  if (ctx.callbackQuery) {
    try {
      await ctx.editMessageText(card.content, {
        parse_mode: "Markdown",
        reply_markup: kb,
      });
    } catch {
      await ctx.reply(card.content, {
        parse_mode: "Markdown",
        reply_markup: kb,
      });
    }
    await ctx.answerCallbackQuery();
  } else {
    await ctx.reply(card.content, {
      parse_mode: "Markdown",
      reply_markup: kb,
    });
  }
}
